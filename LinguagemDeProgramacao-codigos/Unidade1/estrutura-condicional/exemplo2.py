# Estrutura condicional composta
nota1 = 10
nota2 = 20

if nota1 < nota2:
    print(f"A nota1:({nota1}) é menor que a nota2:({nota2})")
else:
    print(f"A nota1:({nota1}) não é menor que a nota2:({nota2})")

soma = nota1 + nota2
media = soma / 2
print(f"Soma das notas: {soma}; Média das notas: {media}")
