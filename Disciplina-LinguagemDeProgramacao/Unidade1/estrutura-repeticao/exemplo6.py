# Exemplo de uso do break

disciplina = "Linguagem de Programação"
for c in disciplina:
   if c == 'a':
      break
   else:
      print(c)

print()

# Exemplo de uso do continue
disciplina = "Linguagem de Programação"
for c in disciplina:
   if c == 'a':
      continue
   else:
      print(c)

